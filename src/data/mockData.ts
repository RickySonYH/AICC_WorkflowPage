// [advice from AI] 노드 타입 정의
export type NodeType = 'start' | 'message' | 'question' | 'condition' | 'api' | 'end'

export interface WorkflowNode {
  id: string
  type: NodeType
  label: string
  config?: Record<string, any>
}

export interface WorkflowEdge {
  from: string
  to: string
  condition?: string
}

// [advice from AI] 워크플로우 상세 정보 인터페이스
export interface WorkflowDetail {
  id: string
  name: string
  description: string
  category: string
  stage: string
  createdAt: string
  updatedAt: string
  usage: string
  usageCount: number
  intent: string[]
  status: 'active' | 'inactive' | 'draft'
  logo?: string // 로고 이모지 또는 아이콘
  nodes: WorkflowNode[]
  edges: WorkflowEdge[]
  settings: {
    timeout: number
    retry: number
    fallback: string
  }
  logs: Array<{
    timestamp: string
    event: string
    user: string
  }>
}

